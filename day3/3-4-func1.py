######################################## *args - 形参可变长未知参数（元组类型） ########################################
def record_ppl_2(name, age, *args):
    print('%s is %s now,' % (name, age))
    print(args)


record_ppl_2('Tom', 30)
record_ppl_2('Tom', 20, 175, 60)
######################################## *args - 形参可变长未知参数（元组类型） ########################################


######################################## **kwargs - 形参可变长关键字参数（字典类型） ########################################
def record_ppl_3(name, age, *args, **kwargs):
    print('%s is %s now,' % (name, age))
    print(args)
    print(kwargs)


record_ppl_3('Candy', 20)
record_ppl_3('Candy', 20, 175, 60, city='sz', number=66)

######################################## **kwargs - 形参可变长关键字参数（字典类型） ########################################


