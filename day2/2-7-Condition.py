# if/ elif
a = 10
b = 20
c = 10
if b > a:
    print('b is greater than a')
elif a == c:
    print('a equals c')
elif c < b:
    print('c less than b')


# if/ else
if a == b:
    print('a equals b')
else:
    print('a NOT equals b')
