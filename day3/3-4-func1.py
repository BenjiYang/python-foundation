######################################## 设置默认的参数时，不用列表 ########################################
# 形参位于方法定义()内，实参位于实际调用方法传入的实际参数
# 设置默认的参数时，不用列表

def increase_1(item, l=[]):
    l.append(item)
    print(l)
    return l


increase_1(10)
increase_1(10)
######################################## 设置默认的参数时，不用列表 ########################################


def increase_2(item, L=None):
    if not L:
        L = []
    L.append(item)
    print(L)
    return L


increase_2(5)
increase_2(5)
######################################## 设置默认的参数时，不用列表 ########################################


