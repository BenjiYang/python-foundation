###################################################### list去重复，转set类型； 多个set取并集，再转回list类型 ######################################################
import random

ramdom_list = [random.randrange(10000) for i in range(10000)]
print("生成随机数的长度：%s" % len(ramdom_list))
print("生成随机数的长度（去重复）：%s" % len(set(ramdom_list)))
